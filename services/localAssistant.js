// Détection de la langue
const detectLanguage = (text) => {
  if (!text) return "fr";

  // Arabe
  const arabicPattern = /[\u0600-\u06FF]/;
  if (arabicPattern.test(text)) return "ar";

  // Français (mots courants)
  const frenchWords = [
    "je",
    "tu",
    "il",
    "elle",
    "nous",
    "vous",
    "le",
    "la",
    "les",
    "et",
    "ou",
    "mais",
    "comment",
    "pourquoi",
    "parle",
    "dis",
  ];
  if (frenchWords.some((word) => text.toLowerCase().includes(word)))
    return "fr";

  // Anglais par défaut
  return "en";
};

