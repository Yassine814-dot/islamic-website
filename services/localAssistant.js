// Fonction principale exportée
export const askLocalAssistant = (question, conversationHistory = []) => {
  console.log("🤔 Question:", question);

  try {
    // Détecter la langue
    const lang = detectLanguage(question);
    console.log("🌍 Langue détectée:", lang);

    // Trouver la meilleure réponse
    const response = findBestMatch(question, lang);

    return {
      id: Date.now(),
      text: response,
      sender: "ai",
      timestamp: new Date().toISOString(),
      language: lang,
      offline: true,
    };
  } catch (error) {
    console.error("❌ Erreur:", error);
    return {
      id: Date.now(),
      text: "Désolé, une erreur est survenue.",
      sender: "ai",
      timestamp: new Date().toISOString(),
      error: true,
    };
  }
};
